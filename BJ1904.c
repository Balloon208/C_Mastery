int DP[1000001];

void DPSET()
{
    DP[0]=0;
    DP[1]=1;
    DP[2]=2;

    for(int i=3; i<=1000000; i++)
    {
        DP[i] = ( DP[i-1] + DP[i-2] ) % 15746;
    }
    
    return;
}


